package Day_1_Hometask_1;

public class Day_1_Task1_1 {

    public static void main(String[] args) {
//        Difference between ++i and i++: both operations perform incrementation of number on 1 but
//        ++i operation performs incrementation before evaluating previous value while i++ evaluates previous value
//        and after performs incrementation

        int i = 0;
        int j = 0;
        System.out.println(++i);
        System.out.println(j++);
        System.out.println(i);
        System.out.println(j);
    }
}
